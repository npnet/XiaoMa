package com.xiaoma.assistant.model;

public class SmartBean {
    public String inOutType;
    public String smartList;
    public String myDevice;

    public String getInOutType() {
        return inOutType;
    }

    public void setInOutType(String inOutType) {
        this.inOutType = inOutType;
    }

    public String getSmartList() {
        return smartList;
    }

    public void setSmartList(String smartList) {
        this.smartList = smartList;
    }

    public String getMyDevice() {
        return myDevice;
    }

    public void setMyDevice(String myDevice) {
        this.myDevice = myDevice;
    }
}
