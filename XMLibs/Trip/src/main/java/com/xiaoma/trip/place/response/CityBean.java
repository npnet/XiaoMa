package com.xiaoma.trip.place.response;

public class CityBean {

    /**
     * pinyin : beijing
     * name : 北京
     * id : 1
     */

    private String pinyin;
    private String name;
    private int id;

    public String getPinyin() {
        return pinyin;
    }

    public void setPinyin(String pinyin) {
        this.pinyin = pinyin;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
